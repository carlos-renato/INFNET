package aulas;

public class aula40_PassagemVetoresMetodos
{

	public static void main(String[] args)
	{
		String[] vet = {"a","e","i","o","u"};
		int i = 1;
		
		vogais(vet);
		consoantes(i);
		
		
		System.out.println(vet[0]);
		System.out.print(consoantes(i));
		
				
	}

	public static void vogais(String[] x)
	{
		x[0] = "c";
	}
	
	public static int consoantes(int y)
	{
		return y = 2;
		
	}


}